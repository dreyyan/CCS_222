// Create an array and use methods pop(), push(), and reverse()
// ===============================================================================

let array = [1, 2, 3, 4, 5];

// display original array
console.log(`       Original: ${array}`);

// perform push() operation & display
array.push(6);
console.log(`   After push(): ${array}`);

// perform pop() operation & display
array.pop();
console.log(`    After pop(): ${array}`);

// perform reverse() operation & display
array.reverse();
console.log(`After reverse(): ${array}`);